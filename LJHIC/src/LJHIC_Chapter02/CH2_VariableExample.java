package LJHIC_Chapter02;

public class CH2_VariableExample {
	//메인 메도스 선언
	public static void main(String[] args) {
		//10을 변수 value의 초기값으로 지정
		int value = 10;
		value=10;
		
		//변수 value값을 읽고 10을 더한는 산술 연산을 수행
		//연산의 결과값을 변수 result의 초기값으로 저장
		int result = value + 10;
		
		//변수 result 값을 읽고 콘솔에 출력
		System.out.println(result);
		System.out.println("result");
		
		//변수 value 값을 출력
		System.out.print(value);
		System.out.println(" println은 줄바꾸기 포함");
		
	}

}
