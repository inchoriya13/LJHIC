package LJHIC_Chapter02;

public class Ch2_ByteExample {
	public static void main(String[] args) {
		/*
		 * byte타입은 -128~127 범위의 정수만 사용할 수 있음.
		 * var 변수는 모두 byte 타입으로선언
		 * var1에 -128 대입
		 * var2에 -30 대입
		 * var3에 0 대입
		 * var4에 30대입
		 * var5에 127대입
		 * var6에 128대입
		 * 그리고 각각 변수 값을 출력하세요.
		 */
		
		byte var1 = -128;
		byte var2 = -30;
		byte var3 = 0;
		byte var4 = 30;
		byte var5 = 127;
		
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		System.out.println(var5);
		
		
	}
}
