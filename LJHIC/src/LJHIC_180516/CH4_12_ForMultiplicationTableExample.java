package LJHIC_180516;

public class CH4_12_ForMultiplicationTableExample {

	public static void main(String[] args) {
		
			
			for(int j=1;j<=9;j++) {
				for(int i=1;i<=9; i++) {
				System.out.print(i + " * " + j + " = " + (i*j)+ "    ");
			}
				System.out.println();
		}

	}

}
